void Delay100ms(unsigned long count){unsigned long volatile time;
  while(count>0){
    time = t;  // 0.1sec at 80 MHz
    while(time){
				time--;
    }
    count--;
  }
}

void Get_Per()
{
	unsigned int i=0;
	for( i=0; i<5; i++)
	{
		Permutation_Gen(i,1);
	}

}

void Permutation_Gen(int idx,int cnt )
{
	unsigned int i=0;
	if(vis[idx])
		return ;
	vis[idx]=cnt;
	if(cnt==5)
	{
		for( i=0; i<5; i++)
		{
			per[n][i]=vis[i];
		}
		n++;

	}
	for( i=0; i<5; i++)
	{
		Permutation_Gen(i,cnt+1);
	}
	vis[idx]=0;
}


void Init_Game()
{
	unsigned int i=0;
	arr[0]=1;
	for(i=1;i<5;i++)
	{
		arr[i]=ENEMYW*i+1;
		
	}
	racer.life=3;
	t=ch;	
	score=0;
	
}


void Init_enemy()
{
	
	unsigned int i=0, x=rand();
	x%=120;
	for( i=0; i<3; i++)
	{
		en[i].posx=arr[per[x][i]];
		en[i].posy=0;
	}
}


void move_enemy()
{
	unsigned int i=0;
	for( i=0; i<3; i++)
	{
		en[i].posy++;
	}
	if(en[0].posy==48)
	{
		Init_enemy();
	}
}


